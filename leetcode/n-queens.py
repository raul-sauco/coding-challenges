class FasterSolution:
    def solveNQueens(self, n: int) -> List[List[str]]:
        board = [["."] * n for i in range(n)]
        res = []
        col = set()
        diag = set()        # r+c
        xdiag = set()       # r-c

        def solve(r):
            if r == n:
                copy = ["".join(row) for row in board]
                res.append(copy)
                return
            for c in range(n):
                if c in col or r+c in diag or r-c in xdiag:
                    continue
                col.add(c)
                diag.add(r+c)
                xdiag.add(r-c)
                board[r][c] = 'Q'
                solve(r+1)
                board[r][c] = '.'
                col.remove(c)
                diag.remove(r+c)
                xdiag.remove(r-c)

        solve(0)
        return res


